"""
Boltz-2 structure prediction for all 4 models (SFT, DPO, GDPO, GDPO_SFT).

Reads top-1 nanobody sequences from boltz_candidates.json (generated by the
Figure 4c notebook cell).  Each of the 4 focus targets × 4 models = 16 complexes.

Usage (inside Docker):
    python src/binder_design/protgpt2_dpo/analysis/run_boltz.py

Results saved to:
    src/binder_design/protgpt2_dpo/analysis/boltz_outputs/
"""

import json
import subprocess
from pathlib import Path

# ── Paths ──────────────────────────────────────────────────────────────────
BASE       = Path("/app/output/analysis")
INPUT_DIR  = BASE / "boltz_inputs"
OUTPUT_DIR = BASE / "boltz_outputs"
CAND_JSON  = BASE / "boltz_candidates.json"

INPUT_DIR.mkdir(parents=True, exist_ok=True)
OUTPUT_DIR.mkdir(parents=True, exist_ok=True)

# ── Load candidates from notebook export ──────────────────────────────────
if not CAND_JSON.exists():
    raise FileNotFoundError(
        f"{CAND_JSON} not found.\n"
        "Run the Figure 4c notebook cell first — it saves boltz_candidates.json"
    )

with open(CAND_JSON) as f:
    candidates = json.load(f)

print(f"Loaded {len(candidates)} candidates from {CAND_JSON.name}")
for c in candidates:
    print(f"  {c['id']:25s}  Tm={c['tm']:.2f}  II={c['ii']:.2f}  len={len(c['nb_seq'])}")


def make_yaml(cand: dict) -> Path:
    """Write a Boltz-2 YAML input file for one nanobody+antigen complex."""
    name = cand["id"]
    yaml_content = (
        f"version: 1\n"
        f"# {name}  Tm={cand['tm']:.2f}  II={cand['ii']:.2f}  {cand['note']}\n"
        f"sequences:\n"
        f"  - protein:\n"
        f"      id: A\n"
        f"      sequence: {cand['nb_seq']}\n"
        f"      msa: empty\n"
        f"  - protein:\n"
        f"      id: B\n"
        f"      sequence: {cand['epitope']}\n"
        f"      msa: empty\n"
    )
    out_path = INPUT_DIR / f"{name}.yaml"
    out_path.write_text(yaml_content)
    return out_path


# ── Generate YAML files ────────────────────────────────────────────────────
yaml_jobs = []
for c in candidates:
    path = make_yaml(c)
    yaml_jobs.append((c["id"], path))
    print(f"  Created: {path.name}")

print(f"\nCreated {len(yaml_jobs)} YAML input files in {INPUT_DIR}\n")

# ── Run Boltz-2 predictions ────────────────────────────────────────────────
print("=" * 65)
print("Running Boltz-2 predictions  (all 4 models × 4 targets = 16 jobs)")
print("Protein-protein interface quality → protein_iptm in confidence JSON")
print("=" * 65)

failed = []
for name, yaml_path in yaml_jobs:
    out_dir  = OUTPUT_DIR / name
    pred_dir = out_dir / f"boltz_results_{name}" / "predictions" / name
    existing = (list(pred_dir.glob("*_model_0.cif")) +
                list(pred_dir.glob("*_model_0.pdb"))) if pred_dir.exists() else []
    if existing:
        print(f"\n>> {name}  [SKIP — already done: {existing[0].name}]")
        continue

    out_dir.mkdir(exist_ok=True)

    cmd = [
        "boltz", "predict", str(yaml_path),
        "--out_dir",           str(out_dir),
        "--accelerator",       "gpu",
        "--devices",           "1",
        "--recycling_steps",   "3",
        "--diffusion_samples", "3",
        "--output_format",     "mmcif",
        "--use_potentials",
        "--write_full_pae",
        "--override",
    ]

    print(f"\n>> {name}")
    print("   " + " ".join(cmd))

    result = subprocess.run(cmd, capture_output=False, text=True)
    if result.returncode != 0:
        print(f"   FAILED (exit {result.returncode})")
        failed.append(name)
    else:
        print(f"   Done -> {out_dir}")

# ── Summary with confidence scores ────────────────────────────────────────
print("\n" + "=" * 65)
print(f"Completed: {len(yaml_jobs) - len(failed)}/{len(yaml_jobs)}")
if failed:
    print(f"Failed: {failed}")

print("\n── Confidence scores (higher protein_iptm = better interface) ──")
print(f"{'Name':<28} {'conf':>6} {'iptm':>6} {'ptm':>6} {'plddt':>7}")
print("-" * 60)
for name, _ in yaml_jobs:
    conf_files = sorted(
        OUTPUT_DIR.glob(f"{name}/**/confidence_{name}_model_*.json")
    )
    if not conf_files:
        print(f"{name:<28}  (no confidence file found)")
        continue
    for conf_file in conf_files:
        with open(conf_file) as f:
            c = json.load(f)
        model_n = conf_file.stem.split("_model_")[-1]
        print(f"{name:<28} {c.get('confidence_score',0):>6.3f} "
              f"{c.get('protein_iptm',0):>6.3f} {c.get('ptm',0):>6.3f} "
              f"{c.get('complex_plddt',0):>7.3f}  model_{model_n}")
